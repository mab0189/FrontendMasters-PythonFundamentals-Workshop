a = 1
b = 2
c = 0

# And:
# If a is true than the value of b is returned
# If a is false than the value of a is returned
print("a and b:", a and b)
print("c and b:", c and b)

# Or:
# If a is true than the value of a is returned
# If a is false than the value of b is returned
print("a or b:", a or b)
print("c or b:", c or b)


# Not:
# If a is true than the value false is returned
# If a is false then the value true is returned
print("not a:", not a)
print("not c:", not c)